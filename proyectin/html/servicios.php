                <h1>ORTODONCIA</h1>
                <h2>Atención Dental en La Paz</h2>
            </div>
            <p>La ortodoncia es una especialidad de la odontología que se enfoca en el diagnóstico, prevención y corrección de las irregularidades dentales y faciales, específicamente las relacionadas con la alineación de los dientes y la mordida. Su principal objetivo es lograr una oclusión (mordida) adecuada, mejorar la función masticatoria y estética dental, y, en muchos casos, contribuir a la salud general del sistema estomatognático, que incluye los músculos, huesos y articulaciones involucradas en la masticación y la fonación.</p>
            <div class="box" style="background-image: url(https://i.pinimg.com/564x/23/e0/7c/23e07cc1bf0be2c177717f0c4de7ce79.jpg);"></div>
            <p>El tratamiento ortodóntico no solo mejora la apariencia estética de la sonrisa, sino que también puede prevenir problemas dentales futuros, como caries y enfermedades periodontales, al facilitar una mejor higiene bucal. Además, al corregir las maloclusiones, la ortodoncia puede mejorar la función masticatoria y la articulación de las palabras, contribuyendo al bienestar general del paciente.</p>
        </section>

        <section class="tratamientos__card">
            <div class="tratamientos__header">
                <h1>PERIODONCIA</h1>
                <h2>Atención Dental en La Paz</h2>
            </div>
            <p>La periodoncia es una especialidad de la odontología que se centra en la prevención, diagnóstico y tratamiento de enfermedades y condiciones que afectan los tejidos de soporte de los dientes, como las encías, el hueso alveolar, el ligamento periodontal y el cemento radicular. Los periodoncistas tratan enfermedades como la gingivitis y la periodontitis, que pueden llevar a la pérdida de dientes si no se controlan. Además, realizan procedimientos como limpiezas profundas, raspados y alisados radiculares, injertos de encías y cirugías para regenerar el tejido periodontal dañado.</p>
            <div class="box" style="background-image: url(https://i.pinimg.com/564x/a0/55/75/a0557591fd01c75e6b4ca09ea9de7f29.jpg);"></div>
            <p>La periodoncia es crucial no solo para la salud dental, sino también para la salud general del paciente, ya que las enfermedades periodontales han sido vinculadas con problemas sistémicos como enfermedades cardíacas, diabetes y complicaciones en el embarazo. Por ello, el cuidado periodontal adecuado es esencial para mantener una buena salud bucal y prevenir problemas más graves.</p>
        </section>

