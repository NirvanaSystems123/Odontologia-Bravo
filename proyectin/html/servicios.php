                <h1>ENDODONCIA</h1>
                <h2>Atención Dental en La Paz</h2>
            </div>
            <p>La endodoncia es una especialidad de la odontología que se enfoca en el diagnóstico, tratamiento y prevención de enfermedades y lesiones que afectan la pulpa dental, que es el tejido blando en el interior del diente que contiene nervios y vasos sanguíneos. El principal procedimiento en endodoncia es el tratamiento de conducto, que se realiza para salvar dientes que están gravemente dañados o infectados.</p>
            <div class="box" style="background-image: url(https://i.pinimg.com/564x/66/ec/8d/66ec8d0c6f5357888c1f36b9c9fd5b7f.jpg);"></div>
            <p>La endodoncia es fundamental para salvar dientes que, de otro modo, podrían necesitar ser extraídos debido a infecciones, caries profundas o lesiones traumáticas. Al salvar el diente natural, se preserva la función masticatoria, la estética y se evita la necesidad de reemplazos dentales artificiales. Además, el tratamiento de conducto tiene una alta tasa de éxito y permite a los pacientes conservar sus dientes naturales por muchos años más.</p>
