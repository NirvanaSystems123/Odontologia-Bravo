        </section>

        <section class="tratamientos__card">
            <div class="tratamientos__header">
                <h1>IMPLANTOLOGIA ORAL</h1>
                <h2>Atención Dental en La Paz</h2>
            </div>
            <p>La implantología oral es una rama de la odontología especializada en el uso de implantes dentales para reemplazar dientes perdidos o dañados. Los implantes dentales son dispositivos artificiales, generalmente hechos de titanio, que se colocan en el hueso maxilar o mandibular para servir como una raíz dental artificial. Sobre estos implantes, se pueden colocar coronas, puentes o dentaduras para restaurar la funcionalidad y estética de la boca.</p>
            <div class="box" style="background-image: url(https://i.pinimg.com/564x/99/7a/15/997a157749c38f4b866bcb1f9745435f.jpg);"></div>
            <p>La implantología oral ofrece una solución duradera y funcional para la pérdida de dientes, mejorando la capacidad de masticación, la estética y la salud bucal general. Además, los implantes dentales ayudan a preservar el hueso maxilar, evitando su deterioro que puede ocurrir cuando se pierde un diente.</p>
        </section>

        <section class="tratamientos__card">
            <div class="tratamientos__header">
                <h1>BLANQUEAMIENTO DENTAL</h1>
                <h2>Atención Dental en La Paz</h2>
            </div>
            <p>El blanqueamiento dental es un procedimiento estético realizado para mejorar el color de los dientes y eliminar manchas o decoloraciones, logrando una sonrisa más brillante y blanca. Este tratamiento es popular tanto en clínicas dentales como en productos de venta libre para el hogar. El blanqueamiento dental se basa en el uso de agentes químicos que descomponen los compuestos responsables de las manchas y el amarillamiento de los dientes.</p>
            <div class="box" style="background-image: url(https://i.pinimg.com/564x/b6/e0/32/b6e032167106e33ba2681bb9b63ba314.jpg);"></div>
            <p>El blanqueamiento dental es efectivo para tratar manchas superficiales y el amarillamiento general, pero puede no ser adecuado para todas las personas. Los resultados pueden variar según la causa de las manchas, la condición de los dientes y las encías, y el tipo de blanqueamiento utilizado. Es importante realizar una consulta con un dentista antes de iniciar el tratamiento para asegurar que sea adecuado para el caso particular y para evitar posibles efectos secundarios, como sensibilidad dental o irritación de las encías.</p>
        </section>

